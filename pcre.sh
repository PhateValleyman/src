#!/ffp/bin/sh

####Reikia du kartus perkompiliuot!!!i####
set -e
export PATH="/ffp/sbin:/ffp/bin:/usr/sbin:/sbin:/usr/bin:/bin"
GNU_BUILD=arm-ffp-linux-uclibceabi
GNU_HOST=$GNU_BUILD
BUILDDIR=/mnt/HD_a2/build/pcre
PACKAGENAME=pcre
VERSION="8.45"
#SOURCEURL=http://sourceforge.net/projects/${PACKAGENAME}/files/${PACKAGENAME}/${VERSION}/${PACKAGENAME}-${VERSION}.tar.gz
SOURCEURL=http://192.168.1.20:88/MyWeb/COMPILER/finish/pcre-8.45.tar.bz2
