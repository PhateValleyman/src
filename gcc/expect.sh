Description of $PACKAGENAME:
Expect is a tool for automating interactive applications such as telnet, ftp, passwd, fsck, rlogin,
tip, etc. Expect really makes this stuff trivial. It is also useful for testing applications.
License(s):custom (Public Domain)
Version:${VERSION}
Homepage:${HOMEURL}

Depends on these packages:
br2:gcc br2:gcc-solibs br2:uClibc br2:uClibc-solibs br2:tcl s:zlib

EOF
