#!/ffp/bin/sh

set -e
export PATH="/ffp/sbin:/ffp/bin:/sbin:/bin:/usr/sbin:/usr/bin"
export SHELL="/ffp/bin/sh"
GNU_BUILD=arm-ffp-linux-uclibceabi
GNU_HOST=$GNU_BUILD
BUILDDIR=/mnt/HD_a2/build/gettext
